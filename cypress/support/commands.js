import LoginPage from '../integration/PageObjects/LoginPage'; // Ensure correct path to LoginPage

Cypress.Commands.add('login', () => {
    cy.fixture('elogin').then((userData) => {
        const loginPage = new LoginPage();
        cy.log(`Logging in with username: ${userData.username} and password: ${userData.password}`);
        loginPage.navigateToLoginPage();
        loginPage.enterEmail(userData.username);
        loginPage.clickContinue();
        loginPage.enterPassword(userData.password);
        loginPage.clickSignIn();
        
    });
});

